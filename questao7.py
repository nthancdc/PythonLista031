'''
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

num1 = float(input("Me diga um número:"))
num2 = float(input("Me diga um outro número:"))
num3 = float(input("Me diga somente mais um número:"))

media = (num1 + num2 + num3) / 3

print("A média aritmética dos números informados é:",media)